package com.bandwidth.webrtc.examples.helloworld.models;

public class Session {
    private String id;
    private String tag;

    public Session() {}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }
}
